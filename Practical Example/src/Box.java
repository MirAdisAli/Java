class Box {
	double width, height, depth;
	
	Box(Box ob){
		width= ob.width;
		height= ob.height;
		depth= ob.depth;
	}
	Box(double w, double h, double d){
		width= w;
		height= h;
		depth= d;	
	}
	Box(){
		width= height= depth= 1;
	}
	Box(double len){
		width= height= depth= len;
	}
	double volume() {
		return width= height= depth;
	}
}

class BoxWeight extends Box{
	double weight;
	
	BoxWeight(double w, double h, double d, double m){
		width=w;
		height=h;
		depth=d;
		weight=m;
	}
}